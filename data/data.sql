CREATE TABLE presentation (
    id int(11) NOT NULL auto_increment,
    title varchar(100) NOT NULL,
    content varchar(25500) NOT NULL,
    private tinyint(1) DEFAULT 0,
    theme varchar(100) DEFAULT 'default',
    controls tinyint(1) DEFAULT 1, 
    progress tinyint(1) DEFAULT 1,
    history tinyint(1) DEFAULT 1,
    center tinyint(1) DEFAULT 1,
    autoSlide smallint(10) DEFAULT '0',
    keyboard tinyint(1) DEFAULT 1,
    overview tinyint(1) DEFAULT 1,
    looop tinyint(1) DEFAULT 0,
    rtl tinyint(1) DEFAULT 0,
    mouseWeel tinyint(1) DEFAULT 0,
    rollingLinks tinyint(1) DEFAULT 1,
    transition varchar(10) DEFAULT 'default',
    presetationWidth smallint(5),
    presentationHeight smallint(5),
    presentationUnit varchar(10),
    presentationMargin int(4),
    presentationMinScale int(4),
    presentationMaxScale int(4),
    multiplexId varchar (10),
    multiplexSecret varchar (25),
    multiplexUrl varchar(255),
    timestamp TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP, 
  PRIMARY KEY (id)
);
